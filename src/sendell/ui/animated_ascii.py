def get_animated_art(name: str) -> AnimatedArt:
    """
    Get animated art by name.

    Args:
        name: Animation name

    Returns:
        AnimatedArt instance

    Raises:
        KeyError: If animation not found
    """
    if name not in ANIMATED_ARTS:
        raise KeyError(f"Animation '{name}' not found. Available: {list(ANIMATED_ARTS.keys())}")

    return ANIMATED_ARTS[name]


def list_animated_arts() -> list:
    """Get list of all available animated art names"""
    return sorted(ANIMATED_ARTS.keys())
